package com.example.AmateurShipper.Model;

public class PostOrderModel {
    String tennguoigui, tenguoinhan, sdtnguoinhan, phut, km, diemdi, diemden, tienung, tienphi, ghichu;

    public PostOrderModel() {
    }

    public PostOrderModel(String tennguoigui, String tenguoinhan, String sdtnguoinhan, String phut, String km, String diemdi, String diemden, String tienung, String tienphi, String ghichu) {
        this.tennguoigui = tennguoigui;
        this.tenguoinhan = tenguoinhan;
        this.sdtnguoinhan = sdtnguoinhan;
        this.phut = phut;
        this.km = km;
        this.diemdi = diemdi;
        this.diemden = diemden;
        this.tienung = tienung;
        this.tienphi = tienphi;
        this.ghichu = ghichu;
    }

    public String getTennguoigui() {
        return tennguoigui;
    }

    public void setTennguoigui(String tennguoigui) {
        this.tennguoigui = tennguoigui;
    }

    public String getTenguoinhan() {
        return tenguoinhan;
    }

    public void setTenguoinhan(String tenguoinhan) {
        this.tenguoinhan = tenguoinhan;
    }

    public String getSdtnguoinhan() {
        return sdtnguoinhan;
    }

    public void setSdtnguoinhan(String sdtnguoinhan) {
        this.sdtnguoinhan = sdtnguoinhan;
    }

    public String getPhut() {
        return phut;
    }

    public void setPhut(String phut) {
        this.phut = phut;
    }

    public String getKm() {
        return km;
    }

    public void setKm(String km) {
        this.km = km;
    }

    public String getDiemdi() {
        return diemdi;
    }

    public void setDiemdi(String diemdi) {
        this.diemdi = diemdi;
    }

    public String getDiemden() {
        return diemden;
    }

    public void setDiemden(String diemden) {
        this.diemden = diemden;
    }

    public String getTienung() {
        return tienung;
    }

    public void setTienung(String tienung) {
        this.tienung = tienung;
    }

    public String getTienphi() {
        return tienphi;
    }

    public void setTienphi(String tienphi) {
        this.tienphi = tienphi;
    }

    public String getGhichu() {
        return ghichu;
    }

    public void setGhichu(String ghichu) {
        this.ghichu = ghichu;
    }
}
